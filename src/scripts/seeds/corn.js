export const createCorn = (cornObject, cornObject1) => {
let cornArray = []
cornObject = {}
cornObject1 = {}
cornObject.type = 'Corn'
cornObject1.type = 'Corn'
cornObject.height = 180
cornObject1.height = 180
cornObject.output = 6
cornObject1.output = 6
cornArray.push(cornObject, cornObject1)
return cornArray
}